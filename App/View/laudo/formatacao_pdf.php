<h1>Formatação de PDF</h1>
<p>
	Os PDFs que serão anexados ao PDF principal (CERTIFICADO e ART) precisam estar
	no mesmo formato do PDF principal.
	<br>
	Caso ocorra algum erro na hora de gerar o PDF, por favor, siga estes passos!
</p>

1. Acesse:
<a href="https://www.ilovepdf.com/pt/pdf_para_word">
	site para converter PDF em Word
</a>
Insira o PDF que gerou o erro neste site e faça download da versão .docx

<br><br>

2. Abra o arquivo com word.<br><br>
Caso nao possua word, baixe um programa chamado OpenOffice e utilize o OpenOffice Writer<br><br>
3. dentro do editor (Microsoft Word ou OpenOffice Writer) gere um novo PDF deste arquivo.<br><br>
4. Envie este novo PDF para ser anexado no sistema da PREVETEC